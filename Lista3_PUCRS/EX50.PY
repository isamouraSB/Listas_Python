#EX50: Faça um algoritmo que leia 2 valores inteiros e positivos: X e Y. O algoritmo deve calcular e escrever a função potência X Y


# Lendo os valores de X e Y
X = int(input("Digite um valor inteiro e positivo para X: "))
Y = int(input("Digite um valor inteiro e positivo para Y: "))

# Calculando a potência X^Y
potencia = X ** Y

print(f"A potência {X}^{Y} é {potencia}.")

