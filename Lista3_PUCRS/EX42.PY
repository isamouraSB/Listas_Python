#EX42: Faça um algoritmo que calcule a seguinte soma: H = 10 + 10 + 10 + ... + 10
#O algoritmo deve ler um número n (inteiro e positivo) e mostrar o resultado final de H. A soma deve ser calculada apenas uma vez.

# Lendo o número n
n = int(input("Digite um número inteiro e positivo: "))

# Calculando a soma
H = 10 * n

print(f"O resultado final de H é {H}.")
